package com.orne.taismaven;

public class Calculadora {
	
	public int suma (int a, int b) {
		return a+b;
		}
	
	public int resta (int a, int b) {
		return b-a;
	}
	
	public int producto (int a, int b) {
		return a*b;
	}
	
	public double divide (double a, double b) {
		return a/b;
	}

}
